def fetch_earthquakes():
    url = "https://data.tmd.go.th/api/DailySeismicEvent/v1/?uid=api&ukey=api12345"
    response = requests.get(url)

    if response.status_code == 200:
        root = ET.fromstring(response.text)
        earthquakes = []

        for quake in root.findall(".//DailyEarthquakes"):
            origin_thai = quake.findtext("OriginThai", default="‡πÑ‡∏°‡πà‡∏£‡∏∞‡∏ö‡∏∏")
            datetime_thai = quake.findtext("DateTimeThai", default="‡πÑ‡∏°‡πà‡∏£‡∏∞‡∏ö‡∏∏")
            magnitude = quake.findtext("Magnitude", default="‡πÑ‡∏°‡πà‡∏£‡∏∞‡∏ö‡∏∏")
            title_thai = quake.findtext("TitleThai", default="‡πÑ‡∏°‡πà‡∏£‡∏∞‡∏ö‡∏∏")
            depth = quake.findtext("Depth", default="‡πÑ‡∏°‡πà‡∏£‡∏∞‡∏ö‡∏∏")
            latitude = quake.findtext("Latitude", default="‡πÑ‡∏°‡πà‡∏£‡∏∞‡∏ö‡∏∏")
            longitude = quake.findtext("Longitude", default="‡πÑ‡∏°‡πà‡∏£‡∏∞‡∏ö‡∏∏")

            earthquake_data = {
                "OriginThai": origin_thai,
                "DateTimeThai": datetime_thai,
                "Magnitude": magnitude,
                "TitleThai": title_thai,
                "Depth": depth,
                "Latitude": latitude,
                "Longitude": longitude
            }
            earthquakes.append(earthquake_data)

        return earthquakes
    else:
        print(f"Error fetching earthquake data: {response.status_code}")
        return []

